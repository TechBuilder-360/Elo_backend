-- Create "providers" table
CREATE TABLE IF NOT EXISTS "providers" (
  "id" character varying NOT NULL,
  "created_at" timestamptz NOT NULL,
  "updated_at" timestamptz NOT NULL,
  "deleted_at" timestamptz NULL,
  "name" character varying NOT NULL,
  "slug" character varying NOT NULL,
  "active" boolean NOT NULL,
  PRIMARY KEY ("id")
);
-- Create index "providers_name_key" to table: "providers"
CREATE UNIQUE INDEX IF NOT EXISTS "providers_name_key" ON "providers" ("name");
-- Create index "providers_slug_key" to table: "providers"
CREATE UNIQUE INDEX IF NOT EXISTS "providers_slug_key" ON "providers" ("slug");
-- Create "business_features" table
CREATE TABLE IF NOT EXISTS "business_features" (
  "id" character varying NOT NULL,
  "created_at" timestamptz NOT NULL,
  "updated_at" timestamptz NOT NULL,
  "deleted_at" timestamptz NULL,
  "name" character varying NOT NULL,
  "identifier" character varying NOT NULL,
  "require_subscription" boolean NOT NULL DEFAULT false,
  "active" boolean NOT NULL DEFAULT true,
  "min" bigint NOT NULL DEFAULT 0,
  "max" bigint NOT NULL DEFAULT 0,
  "fee" jsonb NOT NULL,
  PRIMARY KEY ("id")
);
-- Create index "business_features_identifier_key" to table: "business_features"
CREATE UNIQUE INDEX IF NOT EXISTS "business_features_identifier_key" ON "business_features" ("identifier");
-- Create index "business_features_name_key" to table: "business_features"
CREATE UNIQUE INDEX IF NOT EXISTS "business_features_name_key" ON "business_features" ("name");
-- Create "businesses" table
CREATE TABLE IF NOT EXISTS "businesses" (
  "id" character varying NOT NULL,
  "created_at" timestamptz NOT NULL,
  "updated_at" timestamptz NOT NULL,
  "deleted_at" timestamptz NULL,
  "category" character varying NOT NULL DEFAULT 'others',
  "name" character varying NOT NULL,
  "about" character varying NULL,
  "logo" character varying NULL,
  "email" character varying NOT NULL,
  "website" character varying NULL,
  "active" boolean NOT NULL DEFAULT false,
  "disabled" boolean NOT NULL DEFAULT true,
  "disabled_at" timestamptz NOT NULL,
  "disable_reason" character varying NULL,
  "verified" boolean NOT NULL DEFAULT false,
  "verified_at" timestamptz NULL,
  PRIMARY KEY ("id")
);
-- Create index "businesses_name_key" to table: "businesses"
CREATE UNIQUE INDEX IF NOT EXISTS "businesses_name_key" ON "businesses" ("name");
-- Create "services" table
CREATE TABLE IF NOT EXISTS "services" (
  "id" character varying NOT NULL,
  "created_at" timestamptz NOT NULL,
  "updated_at" timestamptz NOT NULL,
  "deleted_at" timestamptz NULL,
  "name" character varying NOT NULL,
  "identifier" character varying NOT NULL,
  "provider" character varying NOT NULL,
  "require_subscription" boolean NOT NULL DEFAULT false,
  "active" boolean NOT NULL DEFAULT true,
  "min" bigint NOT NULL DEFAULT 0,
  "max" bigint NOT NULL DEFAULT 0,
  "fee" jsonb NOT NULL,
  PRIMARY KEY ("id")
);
-- Create index "service_identifier" to table: "services"
CREATE UNIQUE INDEX IF NOT EXISTS "service_identifier" ON "services" ("identifier");
-- Create index "services_identifier_key" to table: "services"
CREATE UNIQUE INDEX IF NOT EXISTS "services_identifier_key" ON "services" ("identifier");
-- Create index "services_name_key" to table: "services"
CREATE UNIQUE INDEX IF NOT EXISTS "services_name_key" ON "services" ("name");
-- Create index "services_provider_key" to table: "services"
CREATE UNIQUE INDEX IF NOT EXISTS "services_provider_key" ON "services" ("provider");
-- Create "business_documents" table
CREATE TABLE IF NOT EXISTS "business_documents" (
  "id" bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
  "title" character varying NOT NULL,
  "description" character varying NOT NULL,
  "url" character varying NOT NULL,
  "verified" boolean NOT NULL DEFAULT false,
  "business_business_documents" character varying NOT NULL,
  PRIMARY KEY ("id"),
  CONSTRAINT "business_documents_businesses_business_documents" FOREIGN KEY ("business_business_documents") REFERENCES "businesses" ("id") ON UPDATE NO ACTION ON DELETE NO ACTION
);
-- Create "business_services" table
CREATE TABLE IF NOT EXISTS "business_services" (
  "id" bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
  "title" character varying NOT NULL,
  "description" character varying NOT NULL,
  "business_id" character varying NOT NULL,
  PRIMARY KEY ("id"),
  CONSTRAINT "business_services_businesses_services" FOREIGN KEY ("business_id") REFERENCES "businesses" ("id") ON UPDATE NO ACTION ON DELETE NO ACTION
);
-- Create "users" table
CREATE TABLE IF NOT EXISTS "users" (
  "id" character varying NOT NULL,
  "created_at" timestamptz NOT NULL,
  "updated_at" timestamptz NOT NULL,
  "deleted_at" timestamptz NULL,
  "first_name" character varying NOT NULL,
  "last_name" character varying NOT NULL,
  "password" character varying NOT NULL,
  "middle_name" character varying NULL,
  "display_name" character varying NULL,
  "email_address" character varying NOT NULL,
  "email_verified" boolean NOT NULL DEFAULT false,
  "email_verified_at" timestamptz NULL,
  "phone_number" character varying NULL,
  "avatar" character varying NULL,
  "disabled" boolean NOT NULL DEFAULT false,
  "disable_reason" boolean NULL,
  "verified" boolean NOT NULL DEFAULT false,
  PRIMARY KEY ("id")
);
-- Create index "users_email_address_key" to table: "users"
CREATE UNIQUE INDEX IF NOT EXISTS "users_email_address_key" ON "users" ("email_address");
-- Create "managers" table
CREATE TABLE IF NOT EXISTS "managers" (
  "id" character varying NOT NULL,
  "created_at" timestamptz NOT NULL,
  "updated_at" timestamptz NOT NULL,
  "deleted_at" timestamptz NULL,
  "disabled" boolean NOT NULL DEFAULT false,
  "disable_reason" character varying NULL,
  "disabled_at" timestamptz NULL,
  "business_id" character varying NOT NULL,
  "user_id" character varying NOT NULL,
  PRIMARY KEY ("id"),
  CONSTRAINT "managers_businesses_manages" FOREIGN KEY ("business_id") REFERENCES "businesses" ("id") ON UPDATE NO ACTION ON DELETE NO ACTION,
  CONSTRAINT "managers_users_manages" FOREIGN KEY ("user_id") REFERENCES "users" ("id") ON UPDATE NO ACTION ON DELETE NO ACTION
);
-- Create "permissions" table
CREATE TABLE IF NOT EXISTS "permissions" (
  "id" character varying NOT NULL,
  "created_at" timestamptz NOT NULL,
  "updated_at" timestamptz NOT NULL,
  "deleted_at" timestamptz NULL,
  "name" character varying NOT NULL,
  "description" character varying NOT NULL,
  PRIMARY KEY ("id")
);
-- Create "roles" table
CREATE TABLE IF NOT EXISTS "roles" (
  "id" character varying NOT NULL,
  "created_at" timestamptz NOT NULL,
  "updated_at" timestamptz NOT NULL,
  "deleted_at" timestamptz NULL,
  "name" character varying NOT NULL,
  "description" character varying NOT NULL,
  PRIMARY KEY ("id")
);
-- Create "role_permissions" table
CREATE TABLE IF NOT EXISTS "role_permissions" (
  "role_id" character varying NOT NULL,
  "permission_id" character varying NOT NULL,
  PRIMARY KEY ("role_id", "permission_id"),
  CONSTRAINT "role_permissions_permission_id" FOREIGN KEY ("permission_id") REFERENCES "permissions" ("id") ON UPDATE NO ACTION ON DELETE CASCADE,
  CONSTRAINT "role_permissions_role_id" FOREIGN KEY ("role_id") REFERENCES "roles" ("id") ON UPDATE NO ACTION ON DELETE CASCADE
);
-- Create "socials" table
CREATE TABLE IF NOT EXISTS "socials" (
  "id" character varying NOT NULL,
  "created_at" timestamptz NOT NULL,
  "updated_at" timestamptz NOT NULL,
  "deleted_at" timestamptz NULL,
  "name" character varying NOT NULL,
  "url" character varying NOT NULL,
  "business_id" character varying NOT NULL,
  PRIMARY KEY ("id"),
  CONSTRAINT "socials_businesses_socials" FOREIGN KEY ("business_id") REFERENCES "businesses" ("id") ON UPDATE NO ACTION ON DELETE NO ACTION
);
-- Create "user_documents" table
CREATE TABLE IF NOT EXISTS "user_documents" (
  "id" bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
  "title" character varying NOT NULL,
  "description" character varying NOT NULL,
  "url" character varying NOT NULL,
  "business_user_documents" character varying NOT NULL,
  "user_user_documents" character varying NULL,
  PRIMARY KEY ("id"),
  CONSTRAINT "user_documents_businesses_user_documents" FOREIGN KEY ("business_user_documents") REFERENCES "businesses" ("id") ON UPDATE NO ACTION ON DELETE NO ACTION,
  CONSTRAINT "user_documents_users_user_documents" FOREIGN KEY ("user_user_documents") REFERENCES "users" ("id") ON UPDATE NO ACTION ON DELETE SET NULL
);
